export const updateParent = async (
  currentState: CurrentState,
  data: ParentSchema
) => {
  try {
    // Step 1: Retrieve the existing students for the parent
    const existingParent = await prisma.parent.findUnique({
      where: { id: data.id },
      select: { students: { select: { id: true } } },
    });

    if (!existingParent) {
      throw new Error("Parent not found.");
    }

    // Step 2: Identify the students to connect
    const currentStudentIds = existingParent.students.map((s) => s.id);
    const newStudentIds = data.students || [];
    const studentsToConnect = newStudentIds.filter(
      (studentId) => !currentStudentIds.includes(studentId)
    );

    await prisma.parent.update({
      where: { id: data.id },
      data: {
        username: data.username,
        email: data.email || null,
        name: data.name,
        surname: data.surname,
        phone: data.phone,
        address: data.address,
        ...(data.password && { password: data.password }), // Only update password if provided
        students: {
          connect: studentsToConnect.map((studentId) => ({ id: studentId })),
        },
      },
    });

    return { success: true, error: false };
  } catch (error) {
    console.error("Error updating parent:", error);
    return { success: false, error: true };
  }
};

